package day27_WrapperClasses;

public class WrapperClassesIntro {

    public static void main(String[] args) {

        int num1 = 200;

        //we convert primitive data types into wrapper classes to use them inside map and collection
        //map and collection are data structures that do not accept primitive data types
        //so, we convert
        //autoboxing means converting from data type to wrapper
        //unboxing means converting from wrapper class to data type

        Integer n1 = num1;//autoboxing
        int num2 = n1;//unboxing

        //autoboxing must be within the same type
        //unboxing could be different type

        long num3 = n1;//unboxing to long from int





    }
}
