package day05_Concatenation;

public class StudentInfo {

    public static void main(String[] args) {

        String studentName = "Mehmet Ali Arkun";
        short studentAge = 33;
        char studentGender = 'M';
        String schoolName = "Cydeo";
        String phoneNumber = "(077)09214609";
        boolean isFullTimeStudent = true;
        double GPA = 3.1;

        System.out.println("studentName = " + studentName);
        System.out.println("studentAge = " + studentAge);
        System.out.println("studentGender = " + studentGender);
        System.out.println("phoneNumber = " + phoneNumber);
        System.out.println("schoolName = " + schoolName);
        System.out.println("isFullTimeStudent = " + isFullTimeStudent);
        System.out.println("GPA = " + GPA);




    }
}

