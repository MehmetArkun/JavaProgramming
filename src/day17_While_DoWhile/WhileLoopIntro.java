package day17_While_DoWhile;

public class WhileLoopIntro {

    public static void main(String[] args) {

        if (true){
            System.out.println("Wooden Spoon");//print statement executed for one time
        }

        System.out.println("------------");

        while(true){
            System.out.println("Wooden Spoon");//repeated if stamement -> executes infinitely unless it is false
        }

        // use while loop if you dont know how many times to repeat


    }
}
