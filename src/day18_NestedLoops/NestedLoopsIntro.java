package day18_NestedLoops;

public class NestedLoopsIntro {

    public static void main(String[] args) {

        for (int j = 0; j < 4; j++) {

            for (int i = 0; i < 5; i++) {
                System.out.println("Wooden Spoon");

            }
//one iteration in outer loop causes the full iteration of inner loop

        }
    }
}
