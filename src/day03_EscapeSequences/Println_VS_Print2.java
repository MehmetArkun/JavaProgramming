package day03_EscapeSequences; // this is the package name of this class

public class Println_VS_Print2 {

    public static void main(String[] args) { // this is main method

        System.out.println("Knock Knock"); // gives output with a new line aftwerwards
        System.out.println("Who is this?");
        System.out.println("This is Java");

        System.out.println("------------------");

        System.out.print("Knock Knock"); // this output command does not give a new line afterwards
        System.out.print("Who is this?");
        System.out.println();
        System.out.println("----------");


        System.out.print("Hey dude! ");
        System.out.print("Whats up? ");
        System.out.print("How is it going? ");
        System.out.print("No time no see!!");

    }


}


